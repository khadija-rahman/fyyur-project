import phonenumbers


def validate_phone(form, phone):
    try:
        p = phonenumbers.parse(phone.data)
        if not phonenumbers.is_valid_number(p):
            raise ValidationError('Invalid phone number')
    except phonenumbers.NumberParseException:
        raise ValidationError('Invalid phone number')


